/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

/* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* USER CODE BEGIN SysInit */

  /* Configure LED1 */
  BSP_LED_Init(LED1);
  
  /* Configure LED2 */
  BSP_LED_Init(LED2);
  
  /* Configure LED3 */
  BSP_LED_Init(LED3);
  
  /* Configure SW2 */
  BSP_PB_Init(BUTTON_SW2, BUTTON_MODE_GPIO);
  
  /* Configure SW3 */
  BSP_PB_Init(BUTTON_SW3, BUTTON_MODE_GPIO);
  
  /* SW1 is in conflict with LCDGlass pins and cannot be used in this example */
  
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_LCD_Init();
  /* USER CODE BEGIN 2 */
  
  /* Set all Leds */
  BSP_LED_On(LED1);
  BSP_LED_On(LED2);
  BSP_LED_On(LED3);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  
  /* Demonstrate the blink mode */
  BSP_LCD_GLASS_DisplayString(&hlcd, (uint8_t*)"Bat OK");
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_FULL);
  HAL_Delay(2000);
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_3_4);
  HAL_Delay(2000);
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_1_2);
  HAL_Delay(2000);
  BSP_LCD_GLASS_DisplayString(&hlcd, (uint8_t*)"LowBat");
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_1_4);
  HAL_Delay(3000);
  BSP_LCD_GLASS_BlinkConfig(&hlcd, LCD_BLINKMODE_ALLSEG_ALLCOM, LCD_BLINKFREQUENCY_DIV512);
  HAL_Delay(7000);
  BSP_LCD_GLASS_BlinkConfig(&hlcd, LCD_BLINKMODE_OFF, LCD_BLINKFREQUENCY_DIV512);
  HAL_Delay(1000);
  
  /* Display a long string */
  BSP_LCD_GLASS_ScrollSentence(&hlcd, (uint8_t*)"Hello world   ", 5, 200);
  HAL_Delay(1000);
  
  /* Clear LCD content */
  BSP_LCD_GLASS_Clear(&hlcd);
  
  /* Display some letters */
  for(uint8_t ch = 'A'; ch <= 'Z'; ch++)
  {
    for(uint8_t pos = 0; pos < 6; pos++)
    {
      BSP_LCD_GLASS_DisplayChar(&hlcd, &ch, POINT_OFF,  DOUBLEPOINT_OFF, pos);
    }
    HAL_Delay(200);
  }
  
  /* Display some numbers */
  for(uint8_t ch = '0'; ch <= '9'; ch++)
  {
    for(uint8_t pos = 0; pos < 6; pos++)
    {
      BSP_LCD_GLASS_DisplayChar(&hlcd, &ch, POINT_ON,  DOUBLEPOINT_ON, pos);
    }
    HAL_Delay(200);
  }    

  /* Display the battery level */
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_1_4);
  HAL_Delay(200);
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_1_2);
  HAL_Delay(200);
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_3_4);
  HAL_Delay(200);
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_FULL);
  HAL_Delay(200);
  BSP_LCD_GLASS_BarLevelConfig(&hlcd, BATTERYLEVEL_OFF);
  HAL_Delay(200);

  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

